//for loop
for (let i = 0; i < 5; i++) {
    console.log(i);
}

//nested for loop to print i
for (let i = 0; i < 3; i++) {
    for (let j = 0; j < 2; j++) {
        console.log(`i: ${i}`);  
    }
}

//nested for loop to print j
for (let i = 0; i < 3; i++) {
    for (let j = 0; j < 2; j++) {
        console.log(`j: ${j}`);
    }
}


